    private void btnConsultarDisponibilidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarDisponibilidadActionPerformed
        // TODO add your handling code here:
        double subtotal=0;
        if (!almaterial.isEmpty()) {
            String valor = this.tablaDetalleCompra.getValueAt(this.tablaDetalleCompra.getSelectedRow(), 0).toString();
            almaterial.removeIf(lista -> lista.getMaterial_name().equals(valor));
            this.modelo.setRowCount(0);
        for (Material material1 : almaterial) {
                this.modelo.addRow(new Object[]{
                material1.getMaterial_name(),material1.getUnit_price(),material1.getUnits_in_stock()
            });
            subtotal+= material1.getUnit_price()*material1.getUnits_in_stock();
        }
            txtSubTotal.setText(String.valueOf(subtotal));
            txtTotal.setText(String.valueOf(subtotal*1.13));
        }
        if (almaterial.isEmpty()) {
                txtSubTotal.setText(null);
                txtTotal.setText(null);
                this.habilitador.cambiarEstadoComponentes(this.panelBotonera.getComponents(), false);
        }
    }//GEN-LAST:event_btnConsultarDisponibilidadActionPerformed

    public void busqueda(Supplier psupplier){
        for (Supplier supplier : alSupplier) {
            if (supplier.getCompany_name().equals(psupplier.getCompany_name())) {
                this.supplier = new Supplier();
                this.supplier = supplier;
                this.habilitador.cambiarEstadoComponentes(this.panelProducto.getComponents(), true);
                this.habilitador.cambiarEstadoComponentes(this.panelPago.getComponents(), true);
                this.habilitador.cambiarEstadoComponentes(this.panelBotonera.getComponents(), true);
                this.habilitador.cambiarEstadoComponentes(this.panelProveedor.getComponents(), false);
                this.txtNombreProveedor.setText(psupplier.getCompany_name());
                JOptionPane.showMessageDialog(null, "¡Agregado con éxito!","Mensaje de Sistema",JOptionPane.INFORMATION_MESSAGE);
                break;
            }
        }
    }
