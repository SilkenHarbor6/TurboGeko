    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try
        {
            BillImpleCrud billImpleCrud = new BillImpleCrud();
            this.bill.setAlMaterial(almaterial);
            this.bill.setIva(Float.parseFloat(this.txtIVA.getText()));
            this.bill.setObservations(this.txtObservaciones.getText());
            this.bill.setPurchase_date(new java.sql.Date(new Date().getTime()));
            this.bill.setPurchase_id(0);
            this.bill.setPurchase_status(0);
            this.bill.setSubtotal(Float.parseFloat(this.txtSubTotal.getText()));
            this.bill.setSupplier(supplier);
            this.bill.setTotal(Float.parseFloat(this.txtTotal.getText()));
            billImpleCrud.sebastian(billImpleCrud.ultimo(), bill.getTotal(), Integer.parseInt(txtPlazos.getText()),Float.parseFloat(txtMora.getText()),
                    this.bill.getPurchase_date());
            if (billImpleCrud.ingresar(bill)) {
                JOptionPane.showMessageDialog(null, "¡Exito en la operacion!","Mensaje de Sistema",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "¡Error en uno o más campos!","Mensaje de Sistema",JOptionPane.ERROR_MESSAGE);
            }
            this.lbNCompra.setText(String.valueOf(new BillImpleCrud().ultimo()));
        }catch(NumberFormatException ex){
            
            JOptionPane.showMessageDialog(null, "¡Error en uno o más campos!","Mensaje de Sistema",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

