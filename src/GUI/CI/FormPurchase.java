    public void agregarMaterial(Material material){
        boolean con=false;    
        double subtotal=0;
        for (Material material1 : almaterial) {
            if (material1.getMaterial_name().equals(material.getMaterial_name())) {
                material1.setUnits_in_stock(material1.getUnits_in_stock()+material.getUnits_in_stock());
                con = true;
                break;
            }
        }
        if (con==false) {
            this.almaterial.add(material);
            this.modelo.addRow(new Object[]{
                material.getMaterial_name(),material.getUnit_price(),material.getUnits_in_stock()
            });
        }else{
            this.modelo.setRowCount(0);
            for (Material material1 : almaterial) {
                this.modelo.addRow(new Object[]{
                material1.getMaterial_name(),material1.getUnit_price(),material1.getUnits_in_stock()
            });
            }
        }
        for (Material material1 : almaterial) {
            subtotal+= material1.getUnit_price()*material1.getUnits_in_stock();
        }
        txtSubTotal.setText(String.valueOf(subtotal));
        txtTotal.setText(String.valueOf(subtotal*1.13));
        this.habilitador.cambiarEstadoComponentes(this.panelBotonera.getComponents(), true);
    }
