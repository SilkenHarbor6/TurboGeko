    private void jButton16jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16jButton7ActionPerformed
        // TODO add your handling code here
        int nlote=0;
        try
        {
            if (txtNLote.getText().trim().length()!=0) {
                nlote = Integer.parseInt(txtNLote.getText().trim());
            }
            Material material = new Material(0,supplier.getSupplier_id(), txtNombreMaterial.getText().trim(), Integer.parseInt(txtCantidad.getText().trim()),
                    this.jComboBox16.getSelectedItem().toString(), Float.parseFloat(txtPrecio.getText().trim()),
                    nlote, new Date(this.dtCaducidad.getDate().getTime()), 0);
            this.formPurchase.agregarMaterial(material);

            borrar.borrar(new JTextField[]{txtCantidad,txtNLote,txtNombreMaterial,txtPrecio});
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Campos con caracteres no validos","Mensaje de sistema",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton16jButton7ActionPerformed

