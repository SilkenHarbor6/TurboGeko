    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String valor = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 0).toString();
            for (Supplier supplier1 : alsupplier) {
                if (supplier1.getCompany_name().equals(valor)) {
                    formEditSupplier = new FormEditSupplier(supplier1,this);
                    formEditSupplier.setVisible(true);
                    break;
                }
            }
    }//GEN-LAST:event_btnEditarActionPerformed

