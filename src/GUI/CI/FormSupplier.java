    public void inicializar(){
        this.habilitador.cambiarEstadoComponentes(this.PanelProveedores.getComponents(), false);
        this.supplierImpleCrud = new SupplierImpleCrud();
        this.alsupplier = new ArrayList<>();
        alsupplier = this.supplierImpleCrud.obtener();
        this.modelo.setNumRows(0);
        for (Supplier supplier : alsupplier) {
            this.modelo.addRow(new Object[]{
                supplier.getCompany_name(),supplier.getNrc(),supplier.getNit()
        });
        }
    }
