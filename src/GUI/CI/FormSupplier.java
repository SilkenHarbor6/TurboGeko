    private void btnAgregarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarFacturaActionPerformed
        // TODO add your handling code here:
        String valor = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 0).toString();
        for (Supplier supplier1 : alsupplier) {
            if (supplier1.getCompany_name().equals(valor)) {
                Supplier supplier = new Supplier();
                supplier = supplier1;
                formPurchase.busqueda(supplier);
                this.dispose();
                break;
            }
        }
    }//GEN-LAST:event_btnAgregarFacturaActionPerformed

